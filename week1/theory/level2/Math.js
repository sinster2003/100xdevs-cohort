// random
console.log(Math.random()); // 0 to 1
console.log(Math.floor(Math.random() * 10)); // 1 to 10

// floor
console.log(Math.floor(2.3)); // 2 ---> previous integer

// ceil
console.log(Math.ceil(2.3)); // 3 ---> next integer

// max
const maxElement = Math.max(3, 5, 6);
console.log(maxElement);

// min
const minElement = Math.min(3, 5, 6);
console.log(minElement);

// round
console.log(Math.round(2.3)); // 2

// pow
console.log(Math.pow(6,2)); // 6 ^ 2 = 36

// sqrt
console.log(Math.sqrt(25)); // 5